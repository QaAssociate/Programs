//Find the square ,square root ,and cube of a number.
package com.piedistrict;

public class ScSqrt 
{

	public static void main(String[] args) 
	{
		int i=10;
		System.out.println("Square Of the number :" + i * i);
		System.out.println("Cube Of the number :" + i * i * i);
		System.out.println("Square root Of the number :" + Math.sqrt(i));

	}

}
